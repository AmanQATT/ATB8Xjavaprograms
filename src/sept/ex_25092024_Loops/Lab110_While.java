package sept.ex_25092024_Loops;

public class Lab110_While {
    public static void main(String[] args) {
        // While loop
        // While - A,B,C
        // A -> initialization
        // B -> Condition
        // C -> Increment / Decrement

        int i=0;
        while(i<=10){
            System.out.println(i);
            i++;
        }
        for (int j = 0; j <=10 ; j++) {
            System.out.println(j);

        }
    }
}

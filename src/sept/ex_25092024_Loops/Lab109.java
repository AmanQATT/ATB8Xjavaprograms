package sept.ex_25092024_Loops;

public class Lab109 {
    public static void main(String[] args) {
        for (int i = 0; i <10 ; ++i) {  // Pre increment
            System.out.println(i);
        }
        System.out.println("--");
        for (int i = 0; i <10 ; i++) { // Post increment
            System.out.println(i);
        }
        System.out.println("--");
        for (char i = 'A'; i <='Z' ; i++) {
            //if(i == 'P'){
            //break;
            // }
            System.out.println(i);

        }
    }
}

package sept.ex_16092024_Operators;

public class Lab040_QnA {
    public static void main(String[] args) {
        System.out.println(10 == 10); // == compare true values
        System.out.println(10 >= 10); // 10>10 or 10=10
        System.out.println(10 <= 10); // 10<10 or 10=10
        System.out.println(10 > 10);
        System.out.println(10 < 10);
    }
}

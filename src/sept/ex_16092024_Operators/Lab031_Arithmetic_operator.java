package sept.ex_16092024_Operators;

public class Lab031_Arithmetic_operator {
    public static void main(String[] args) {
        // arithmetic operator
        // +,-,/,*
        int a=10;
        int b=2;
        System.out.println(a+b); // + --> binary - 2 operands
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // % - modulus - gives remainders
        System.out.println(a%b);
        System.out.println(67%13);

         // 13|67|5 ->Quoient- bhaagfal
          //  |65|
           //   2 -> Remainder- seshfal

    }
}

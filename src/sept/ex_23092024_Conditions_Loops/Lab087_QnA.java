package sept.ex_23092024_Conditions_Loops;

public class Lab087_QnA {
    public static void main(String[] args) {
       char code = 'C';
       switch(code){
           default:
               System.out.println("Heloooo");
           case 'A':
               System.out.println("65");
               break;
           case 'B':
               System.out.println("66");
               break;
       }
    }
}
